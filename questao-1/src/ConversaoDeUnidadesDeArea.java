public class ConversaoDeUnidadesDeArea {
    public static float metroParaPe(float metro) {
        return (metro * 10.76f);
    }

    public static float peParaCentimetro(float pe) {
        return (pe * 929);
    }

    public static float milhaParaAcre(float milha) {
        return (milha * 640);
    }

    public static float acreParaPe(float acre) {
        return (acre * 43560);
    }

    public static float metroParaAcre(float metro) {
        return (metro * 0.000247105f);
    }
}
